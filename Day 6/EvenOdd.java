package Day_6;

/*Print even and odd number between 1
100 seperately by using for loop?*/

public class EvenOdd{
public static void main(String[] args){
int n=100;

for(int i=1;i<=n;i++){
if(i%2!=0){System.out.println(i+" is Odd Number");
}
else {
System.out.println(i+" Even Number");}
}}}

